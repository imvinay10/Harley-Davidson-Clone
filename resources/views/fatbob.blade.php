  <h2>About Fat Bob</h2>
Harley-Davidson Fat Bob is a cruiser bikes available at a starting price of Rs. 16,75,000 in India. It is available in only 1 variant and 3 colours. The Fat Bob is powered by 1868cc BS6 engine which develops a power of 92.5 bhp and a torque of 155 Nm. With both front and rear disc brakes, Harley-Davidson Fat Bob comes up with anti-locking braking system.

The 2021 Harley-Davidson Fat Bob is powered by a BS6-compliant, 1,868cc, V-twin Milwaukee-Eight 114 engine. Linked to a six-speed gearbox, the motor makes 92.5bhp of maximum power at 5,020rpm and 155Nm of peak torque at 3,500rpm. In comparison, its predecessor was powered by a 1,745cc motor.

While the mechanical specifications have undergone a major upgrade, the styling remains similar to its predecessor. Thus, the 2021 Harley-Davidson Fat Bob continues to feature an all-LED rectangular headlamp at the front, a flat handlebar, a semi-digital instrument cluster, split-style seats, twin-exhaust cannisters, and fat tyres. The 2-into-1-into-2 pipes get a bronze finish while the exhaust cannisters feature a silver tone. Most components of the motorcycle get a blacked-out look which enhances the overall styling of the Fat Bob.

The 2021 Fat Bob tips the scale at 306kg, which is three kilograms lighter than the old model. The hardware on the motorcycle includes upside-down front forks upfront and a rear monoshock to perform the suspension tasks. The braking duties are handled by dual discs at the front and a single rotor at the rear.

The Harley-Davidson Fat Bob is available in three paint schemes – Vivid Black, Billiard Red, and Deadwood Green Denim. The Billiard Red and Deadwood Green Denim colour options carry a marginal price premium over the Vivid Black shade.</p>


